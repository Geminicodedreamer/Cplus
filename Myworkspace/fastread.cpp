#include <bits/stdc++.h>
using namespace std;

template <typename T>
inline T fastread(T &x)
{
    char c = getchar();
    x = 0;
    T s = 1;
    while (c < '0' || c > '9')
    {
        if (c == '-')
            s = -1;
        c = getchar();
    } // 是符号
    while (c >= '0' && c <= '9')
    {
        x = x * 10 + c - '0';
        c = getchar();
    } // 是数字
    return x = x * s;
}
int main()
{
    int n;
    while (1)
    {
        fastread(n);
        cout << n << '\n';
    }
}